    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:
        Pharmacy pharmacy = (Pharmacy) pharmacyNamePicker.getSelectedItem();
        int selectedRowIndex = tblPharmacyName.getSelectedRow();

        if (selectedRowIndex < 0) {
            Toast toast = new Toast(mainPanel, "Please select a row to modify", false);
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblPharmacyName.getModel();
        UserAccount us = (UserAccount) model.getValueAt(selectedRowIndex, 0);
        txtPharmacyInputUserName.setText(us.getUsername());
        txtPassword.setText(us.getPassword());
        txtAdminInputName.setText(us.getEmployee().getName());
        pharmacy.getUserAccountDirectory().removeUser(us);
        btnDelete.setEnabled(false);
        btnModify.setEnabled(false);
    }//GEN-LAST:event_btnModifyActionPerformed

