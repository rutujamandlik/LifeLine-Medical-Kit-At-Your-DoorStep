    private void populateTable(Pharmacy pharmacy) {
        DefaultTableModel model = (DefaultTableModel) tblPharmacyName.getModel();
        model.setRowCount(0);

        String dm = "Pharmacy Admin";

        for (UserAccount us : pharmacy.getUserAccountDirectory().getUserAccountList()) {
            if (us.getRole().toString().equals(dm)) {
                Object[] row = new Object[2];
                row[0] = us;
                row[1] = us.getEmployee().getName();
                model.addRow(row);
            }

        }
    }

    private void populateComboBox() {
        pharmacyNamePicker.removeAllItems();
        int i = 0;
        for (Pharmacy pharmacy : enterprise.getPharmacyDirectory().getPharmacyStoresArray()) {
            if (i == 0) {
                populateTable(pharmacy);
                i++;
                System.out.println(pharmacy.getPharmacyStoreName());
            }
            pharmacyNamePicker.addItem(pharmacy);
        }
    }

