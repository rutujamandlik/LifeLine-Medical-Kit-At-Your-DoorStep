    private void saveData() {
        Pharmacy pharmacy = (Pharmacy) pharmacyNamePicker.getSelectedItem();

        String username = txtPharmacyInputUserName.getText();
        String password = txtPassword.getText();
        String name = txtAdminInputName.getText();

        Employee employee = pharmacy.getEmployeeDirectory().createEmployee(name);
        UserAccount ua = pharmacy.getUserAccountDirectory().createUserAccount(username, password, employee, new PharmacyAdminRole());

        populateTable(pharmacy);
        txtPharmacyInputUserName.setText("");
        txtPassword.setText("");
        txtAdminInputName.setText("");
        btnDelete.setEnabled(true);
        btnModify.setEnabled(true);
        
        Toast toast = new Toast(mainPanel, "Pharmacy admin added successfully", true);
    }
    
