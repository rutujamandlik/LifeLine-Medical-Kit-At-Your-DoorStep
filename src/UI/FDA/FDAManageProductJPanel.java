    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblProducts.getModel();
        model.setRowCount(0);
        
        for(Enterprise enterprise: warehouseEnterpriseArray){
            for(Warehouse warehouse: enterprise.getWarehouseDirectory().getWarehouseDirectoryArray()){
                ArrayList<WorkRequest> workReq = warehouse.getWorkQueue().getWorkRequestListArray3();
                for(int i=workReq.size()-1; i>=0; i--){
                    FDAApprovalRequest req = (FDAApprovalRequest)workReq.get(i);
                    Object[] row = new Object[4];
                    row[0] = req;
                    row[1] = req.getProduct();
                    row[2] = req.getWarehouse();
                    row[3] = req.getStatus();
                    model.addRow(row);
                }
            }
        }
    }    
    
