    private void btnDenyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDenyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            Toast toast = new Toast(mainPanel, "Please select a row to deny", false);
            return;
        }
        FDAApprovalRequest request = (FDAApprovalRequest) tblProducts.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Waiting")) {
            request.setStatus("Denied");
            request.getProduct().setStatus("Denied");
            
            populateTable();
            Toast toast = new Toast(mainPanel, "Product denied successfully", true);
            
        } else {
            Toast toast = new Toast(mainPanel, "Product already approved or denied", false);
            return;
        }
    }//GEN-LAST:event_btnDenyActionPerformed

    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            Toast toast = new Toast(mainPanel, "Please select a row to approve", false);
            return;
        }
        FDAApprovalRequest request = (FDAApprovalRequest) tblProducts.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Waiting")) {
            request.setStatus("Approved");
            request.getProduct().setStatus("Approved");
            
            populateTable();
            Toast toast = new Toast(mainPanel, "Product approved successfully", true);
        } else {
            Toast toast = new Toast(mainPanel, "Product already approved or denied", false);
            return;
        }
    }//GEN-LAST:event_btnApproveActionPerformed


