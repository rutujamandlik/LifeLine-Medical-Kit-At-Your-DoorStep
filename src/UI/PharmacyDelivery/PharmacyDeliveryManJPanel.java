    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed

       int selectedRowIndex = tblProcess.getSelectedRow();
        if(selectedRowIndex<0){
            Toast toast = new Toast(mainPanel, "Please select a row to view", false);
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tblView.getModel();
        model.setRowCount(0);

        PharmacyAdminOrderWorkRequest request = (PharmacyAdminOrderWorkRequest)tblProcess.getValueAt(selectedRowIndex, 0);

        model.setRowCount(0);
        for(Product product: request.getOrder().getItems()){
            Object[] row = new Object[3];
            row[0] = product;
            row[1] = request.getOrder().getPrice();
            row[2] = request.getOrder().getQuantity();
            model.addRow(row);
        }
    }//GEN-LAST:event_btnViewActionPerformed

