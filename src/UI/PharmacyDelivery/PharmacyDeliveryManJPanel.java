    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblProcess.getModel();
        model.setRowCount(0);
        ArrayList<WorkRequest> workReq = distribution.getWorkQueue().getWorkRequestListArray2();
        
        for(int i=workReq.size()-1; i>=0; i--){
            PharmacyAdminOrderWorkRequest req = (PharmacyAdminOrderWorkRequest)workReq.get(i);

            if(req.getReceiver()!=account){
                continue;
            }
            if(req.getStatus().equals("Completed")){
                continue;
            }
            Object[] row = new Object[4];
            row[0] = req;
            row[1] = req.getSender().getEmployee().getName();
            row[2] = req.getReceiver() == null ? null : req.getReceiver().getEmployee().getName();
            row[3] = req.getStatus();
            model.addRow(row);
        }
    }
    
