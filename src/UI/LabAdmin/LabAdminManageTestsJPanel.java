    private void populatePicker(){
        labTechnicianPicker.removeAllItems();
        
        String role = "Lab Technician";
        
        for (UserAccount ua:hospital.getUserAccountDirectory().getUserAccountList()) {
            if (ua.getRole().toString().equals(role)) {
                labTechnicianPicker.addItem(ua);
            }
        }
    }
    
    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblTests.getModel();
        model.setRowCount(0);
        
        ArrayList<WorkRequest> workReq = hospital.getWorkQueue().getWorkRequestListArray2();
        for (int i=workReq.size()-1; i>=0; i--) {
            
            TestRequest lt = (TestRequest)workReq.get(i);
            if (lt.getCustomer() == null) {
                continue;
            }
            Product product = lt.getProduct();
                Object[] row = new Object[4];
                row[0] = lt;
                row[1] = product.getProductName();
                row[2] = lt.getCustomer().getEmployee().getName();
                row[3] = lt.getStatus();
                model.addRow(row);
        }
    }

