    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        int selectedRow = tblTests.getSelectedRow();
        if (selectedRow < 0) {
            Toast toast = new Toast(mainPanel, "Please select a row to assign", false);
            return;
        }

        TestRequest request = (TestRequest) tblTests.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Accepted")) {
            
            UserAccount ua1 = (UserAccount)labTechnicianPicker.getSelectedItem();
            request.setReceiver(ua1);
            request.setStatus("Sent for testing");
            populateTable();
            
            Toast toast = new Toast(mainPanel, "Sample sent successfully", true);
        } else {
            if (request.getStatus().equals("Sent")) {
                Toast toast = new Toast(mainPanel, "Please accept this request to assign", false);
            } else {
                Toast toast = new Toast(mainPanel, "Request has been previously assigned/ rejected", false);
            }
        }

    }//GEN-LAST:event_btnAssignActionPerformed

    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblTests.getSelectedRow();
        if (selectedRow < 0){
            Toast toast = new Toast(mainPanel, "Please select a row to accept", false);
            return;
        }

        WorkRequest request = (WorkRequest) tblTests.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Sample sent")) {
            request.setStatus("Accepted");
            
            Toast toast = new Toast(mainPanel, "Test accepted successfully", true);
        } else {
            Toast toast = new Toast(mainPanel, "Request has been previously accepted/ rejected", false);
        }
//        populateTable();
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed

        int selectedRow = tblTests.getSelectedRow();
        if (selectedRow < 0) {
            Toast toast = new Toast(mainPanel, "Please select a row to reject", false);
            return;
        }

        WorkRequest request = (WorkRequest) tblTests.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Sample sent")) {
            request.setStatus("Rejected");
            
            Toast toast = new Toast(mainPanel, "Test rejected successfully", true);
        } else {
            Toast toast = new Toast(mainPanel, "Request has been previously accepted/ rejected", false);
        }
        populateTable();
    }//GEN-LAST:event_btnRejectActionPerformed


